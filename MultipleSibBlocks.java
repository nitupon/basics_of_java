package Package1;

public class MultipleSibBlocks {
	
	static
	{
		System.out.println("This is a SIB");
	}
	
	static
	{
		System.out.println("This is SIB no. 2");
	}
	
	static
	{
		System.out.println("This is SIB no. 3");
	}

	public static void main(String[] args) {
		System.out.println("Hence multiple SIB blocks are possible");

	}

}
