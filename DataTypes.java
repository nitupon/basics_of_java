package Package1;

public class DataTypes {
	
	public static void DatatypeByte() {
		System.out.println("Byte DT range is 128 to 127");  //-2^8-1 to 2^8-1 - 1
	}
	
	public static void DatatypeShort() {
		System.out.println("Short DT range is -32768 to +32767"); //-2^16-1 to +2^16-1 - 1
	}
	
	public static void DatatypeInt() {
		System.out.println("This is a int datatype ");
	}
	
	public static void DatatypeLong() {
		System.out.println("This is a long datatype");
	}
	
	public static void DatatypeFloat() {
		
		System.out.println("This is a float datatype");
	}
	
	public static void DatatypeDouble() {
		System.out.println("This is double datatype");
	}
	
	public static void DatatypeChar() {
		System.out.println("This is a char datatype");
	}
	
	public static void DatatypeBoolean() {
		System.out.println("This is a boolean datatype");
	}
	
	public static void DatatypeString() {
		System.out.println("This is a string datatype");
	}
	
	public static void main(String[] args) {
	
		DatatypeByte();
		DatatypeShort();
		DatatypeInt();
		DatatypeLong();
		DatatypeFloat();
		DatatypeDouble();
		DatatypeChar();
		DatatypeBoolean();
		DatatypeString();
		System.out.println("So there are 9 data types in java");
		
		
		

	}

}
